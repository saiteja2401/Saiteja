-- ADD CUSTOMER USING PROCEDURE
 CREATE PROCEDURE ADD_CUSTOMER @CID CHAR(5), @CNAME VARCHAR(50), @ADDRESS VARCHAR(80), @CITY VARCHAR(50), @PHONE CHAR(15), @EMAIL VARCHAR(50), @DOB DATE
 AS 
 BEGIN
	INSERT INTO CUST
	VALUES(@CID, @CNAME, @ADDRESS, @CITY, @PHONE, @EMAIL, @DOB);

	SELECT * FROM CUST
	WHERE CID = @CID;
END;


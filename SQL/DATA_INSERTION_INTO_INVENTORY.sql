-- ADD ORDER USING PROCEDURE
 CREATE PROCEDURE ADD_ORDER @OID CHAR(5), @CID CHAR(5), @PID CHAR(5), @OQTY INT
 AS 
 BEGIN
	INSERT INTO ORDERS
	VALUES(@OID, CONVERT(VARCHAR(50), GETDATE(), 111), @CID, @PID, @OQTY);

	SELECT * FROM ORDERS
	WHERE OID = @OID;
END;


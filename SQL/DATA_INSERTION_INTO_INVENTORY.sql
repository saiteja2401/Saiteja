 USE INVENTORY

 -- ADD SUPPLIER USING PROCEDURE
 CREATE PROCEDURE ADD_SUPPLIER @SID CHAR(5), @SNAME VARCHAR(50), @SADD VARCHAR(60), @SCITY VARCHAR(30), @SPHONE VARCHAR(15), @EMAIL VARCHAR(80)
 AS 
 BEGIN
	INSERT INTO SUPPLIER 
	VALUES(@SID, @SNAME, @SADD, @SCITY, @SPHONE, @EMAIL);

	SELECT * FROM SUPPLIER
	WHERE SID = @SID;
END;

-- ADD PRODUCT USING PROCEDURE
 CREATE PROCEDURE ADD_PRODUCT @PID CHAR(5), @PDESC VARCHAR(50), @PRICE INT, @CATEGORY VARCHAR(5), @SID CHAR(5)
 AS 
 BEGIN
	INSERT INTO PRODUCT
	VALUES(@PID, @PDESC, @PRICE, @CATEGORY, @SID);

	SELECT * FROM PRODUCT
	WHERE PID = @PID;
END;


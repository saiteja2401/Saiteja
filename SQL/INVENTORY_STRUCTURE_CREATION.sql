USE INVENTORY;

--SUPPLIER TABLE
CREATE TABLE SUPPLIER(
SID CHAR(5) PRIMARY KEY,
SNAME CHAR(30) NOT NULL,
SADD CHAR(100) NOT NULL,
SCITY CHAR(50) DEFAULT 'DELHI',
SPHONE CHAR(15) UNIQUE,
EMAIL CHAR(50)
)

--PRODUCT TABLE
CREATE TABLE PRODUCT(
PID CHAR(5) PRIMARY KEY,
PDESC CHAR(100) NOT NULL,
PRICE INT CHECK (price>0),
CATEGORY CHAR(5) CHECK (CATEGORY IN ('IT', 'HA', 'HC')),
SID CHAR(5) REFERENCES SUPPLIER(SID),
)

--CUSTOMER TABLE
CREATE TABLE CUST(
CID CHAR(5) PRIMARY KEY,
CNAME CHAR(30) NOT NULL,
ADDRESS CHAR(100) NOT NULL,
CITY CHAR(30) NOT NULL,
PHONE CHAR(15) NOT NULL,
EMAIL CHAR(30) NOT NULL,
DOB DATE CHECK (DOB < '01-JAN-2000')
)

-- STOCK TABLE
CREATE TABLE STOCK(
PID CHAR(5) REFERENCES PRODUCT(PID),
SQTY INT CHECK (SQTY>=0),
ROL INT CHECK (ROL>0),
MOQ INT CHECK (MOQ>=5)
)

-- ORDERS TABLE
CREATE TABLE ORDERS (
OID CHAR(5) PRIMARY KEY,
ODATE DATE,
CID CHAR(5) REFERENCES CUST(CID),
PID CHAR(5) REFERENCES PRODUCT(PID),
OQTY INT CHECK (OQTY>=1)
)

